"ineciaciones  de primer Grado"
syms x
solve (7x+5<2x-10)
solve  (-6*x+9>0)
solve  (x*2 +3*x +3 >0)

"inecuaciones de segundo Grado"
solve (x^2-6*x-7>0)
solve (3*x^3-x<3*x-1)
"innecuaciones valor absoluto"
abs (x+7)
abs  (5*x+6)
